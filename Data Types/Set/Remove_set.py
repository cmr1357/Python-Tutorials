#Using Remove Method
set1={"apple","Orange","cherry"}
set1.remove("Orange")

print(set1)

#discard method
set1={"apple","Orange","cherry"}
set1.discard("Orange")
print(set1)

#pop method
set1={"apple","Orange","cherry"}
x=set1.pop()

print(x)
print(set1)


set1.clear()

print(set1)


set1={"apple","Orange","cherry"}

del set1
print(set1)

